					<h2 style="color: #337ab7;">Study 3:</h2>
					<h3>Impact of weekly Air2g2 applications on a sand based bemurdagrass athletic field</h3>
				</div>
					<div class="row">
						<div class="col-sm-6">
							<h4>
								This study was completed on an athletic field built to USGA sand specifications with ‘Tifway’ bermudagrass. There were three replications of each treatment and they were applied twice a month in August. The treatments consisted of Air2g2 soil injections and an untreated control. Data collection included surface hardness, soil moisture, water infiltration, percent green cover, dark green color index, color quality, and turfgrass shear strength measurements. The purpose of this study was to see what effect the Air2g2 had on a sand based root zone.
									<br>
									<br>
								<span style="color: #337ab7;">Conclusions:</span> Initially, Air2g2 injections reduced surface hardness measurements from 52 to 37 Gmax using a Clegg Impact Surface Hammer. Turfgrass shear strength was not affected by Air2g2 injections. In addition, no differences were observed for soil moisture content. Also, no differences were found in percent green cover, dark green color index, and color, and quality measurements, respectively. However, Air2g2 injections increase water infiltration rates by 10” per hour. These findings suggest that the Air2g2 decreased the surface hardness and increased water infiltration rates on a sand based bermudagrass athletic field.
