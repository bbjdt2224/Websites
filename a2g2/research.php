					<h2 style="color: #337ab7;">Study 1:</h2>
					<h3>Determination of soil characteristics and application interval of Air2g2 injection on a native soil (silt loam) bermudagrass athletic field</h3>
				</div>
					<div class="row">
						<div class="col-sm-6">
							<h4>This study was completed on a local multi-use native soil (silt loam) athletic field with ‘Tifway’ bermudagrass. The purpose of this study was to determine the optimal application interval and the effect the Air2g2 has on soil characteristics. The treatments consisted of three treatments: Air2g2 applications once per month, Air2g2 applications two times per month, and an untreated control. The treatments were replicated three times from 1 July through 31 August. Data collection included: surface hardness, soil moisture, bulk density, particle density, porosity, percent green cover, dark green color index, color, and quality measurements. Soil cores were collected to determine soil physical properties at the start, middle, and conclusion of the study. Finally, surface hardness measurements were taken before and after each treatment application.
								<br>
								<br>
							<span style="color: #337ab7;">Conclusions:</span> Air2g2 injections, reduced surface hardness by 21% (100 to 82 Gmax). Air2g2 injections did not affect soil moisture, percent green cover, dark green color index, color, quality, and particle density. However, Air2g2 injections reduced soil bulk density from 1.63 to 1.39 g/cm3 and increased total soil porosity from 0.4 to 0.5. This suggests that the Air2g2 helps to reduce soil compaction by decreasing surface hardness. The reductions in soil bulk density and increase total soil porosity suggests that the Air2g2 injections increase macropore (airfilled) space.</h4>
