							<h4>
								Study two was complete on a sand capped ultra-dwarf bermudagrass putting green. This study compared the effect of needle tine versus the Air2g2 in how it affected green speeds. This study consisted of three treatments of a control with no applications, needle tine twice a month and Air2g2 applications twice a month. Green speeds were measured six times on each treatment at each rating date, before and after treatments were applied.
									<br>
									<br>
								<span style="color: #337ab7;">Conclusions:</span> No reduction in green speeds was found with Air2g2 injections. Conversely, green speeds increased by six inches immediately following Air2g2 injections; while, green speeds decreased when needle tine treatments were applied. However, data collection to determine how long the increased green speeds lasted was not collected. Therefore, these results suggest that green speeds are not reduced following Air2g2 injections.
