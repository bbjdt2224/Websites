					<h3>Weekly application of Air2g2 on native soil (silt loam) to detect surface hardness reductions</h3>
				</div>
					<div class="row">
						<div class="col-sm-6">
							<h4>
								This study was completed on a native soil (silt loam) ‘Riviera’ common bermudagrass mown at 7/8” with no irrigation. Treatments were weekly Air2g2 injections and an untreated control. The purpose of this study was to determine if weekly Air2g2 injections reduced surface hardness measurements. Based off of Study one findings, data suggested that Air2g2 injections two times per month were not enough to maintain reduced surface hardness measurements. Therefore, this study investigated weekly Air2g2 injections. This study had three replications of each treatment and was repeated four times throughout the month of August. Surface hardness and soil moisture measurements were collect to determine the duration of surface hardness reduction per application of the Air2g2.
									<br>
									<br>
								<span style="color: #337ab7;">Conclusions:</span> The initial reduction in surface hardness did not persist two weeks after Air2g2 injections. Weekly Air2g2 injections maintained a 19% reduction in surface hardness compared to the control plots. These results suggest that weekly Air2g2 injections are needed to maintain the reduction in surface hardness.
